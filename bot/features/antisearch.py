from __future__ import annotations

# Визуально неотличимые двойники символов из другого алфавита/юникод-блока.
# Ключ — обычная буква, значение — её "двойник" с другим кодпоинтом.
# Поиск по точному тексту (например, в Telegram) не найдёт подменённую строку,
# хотя выглядит она абсолютно так же.
_HOMOGLYPHS: dict[str, str] = {
    # латиница -> кириллица/греческий (визуальные близнецы)
    "a": "а", "A": "А",
    "e": "е", "E": "Е",
    "o": "о", "O": "О",
    "p": "р", "P": "Р",
    "c": "с", "C": "С",
    "x": "х", "X": "Х",
    "y": "у", "Y": "У",
    "i": "і",
    "T": "Т",
    "H": "Н",
    "K": "К",
    "M": "М",
    "B": "В",
    # кириллица -> латиница (для сообщений на русском)
    "а": "a", "А": "A",
    "е": "e", "Е": "E",
    "о": "o", "О": "O",
    "р": "p", "Р": "P",
    "с": "c", "С": "C",
    "х": "x", "Х": "X",
    "у": "y", "У": "Y",
    "і": "i",
    "т": "τ",  # греческая тау
    "к": "κ",  # греческая каппа
}


def antisearch_transform(text: str) -> str:
    """Подменяет буквы на визуальные двойники из другого алфавита.

    Результат выглядит идентично оригиналу, но из-за разных кодпоинтов
    обычный поиск по подстроке ищущего оригинальный текст ничего не найдёт.
    """
    return "".join(_HOMOGLYPHS.get(char, char) for char in text)
